void adc1Init(){

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_ADC1, ENABLE);

	ADC_CommonInitTypeDef ADC_CommonInitStructure;
	// niezaleny tryb pracy przetwornikw
	ADC_CommonInitStructure.ADC_Mode = ADC_Mode_Independent;
	// zegar gwny podzielony przez 2
	ADC_CommonInitStructure.ADC_Prescaler = ADC_Prescaler_Div2;
	// opcja istotna tylko dla trybu multi ADC
	ADC_CommonInitStructure.ADC_DMAAccessMode = ADC_DMAAccessMode_Disabled;
	// czas przerwy pomidzy kolejnymi konwersjami
	ADC_CommonInitStructure.ADC_TwoSamplingDelay = ADC_TwoSamplingDelay_5Cycles;
	ADC_CommonInit(&ADC_CommonInitStructure);

	ADC_InitTypeDef ADC_InitStructure;
	//ustawienie rozdzielczoci przetwornika na maksymaln (12 bitw)
	ADC_InitStructure.ADC_Resolution = ADC_Resolution_12b;
	//wyczenie trybu skanowania (odczytywa bdziemy jedno wejcie ADC
	//w trybie skanowania automatycznie wykonywana jest konwersja na wielu //wejciach/kanaach)
	ADC_InitStructure.ADC_ScanConvMode = DISABLE;
	//wczenie cigego trybu pracy
	ADC_InitStructure.ADC_ContinuousConvMode = ENABLE;
	//wyczenie zewntrznego wyzwalania
	//konwersja moe by wyzwalana timerem, stanem wejcia itd. (szczegy w //dokumentacji)
	ADC_InitStructure.ADC_ExternalTrigConv = ADC_ExternalTrigConv_T1_CC1;
	ADC_InitStructure.ADC_ExternalTrigConvEdge = ADC_ExternalTrigConvEdge_None;
	//warto binarna wyniku bdzie podawana z wyrwnaniem do prawej
	//funkcja do odczytu stanu przetwornika ADC zwraca warto 16-bitow
	//dla przykadu, warto 0xFF wyrwnana w prawo to 0x00FF, w lewo 0x0FF0
	ADC_InitStructure.ADC_DataAlign = ADC_DataAlign_Right;
	//liczba konwersji rwna 1, bo 1 kana
	ADC_InitStructure.ADC_NbrOfConversion = 1;
	// zapisz wypenion struktur do rejestrw przetwornika numer 1
	ADC_Init(ADC1, &ADC_InitStructure);

	ADC_RegularChannelConfig(ADC1, ADC_Channel_1, 1, ADC_SampleTime_84Cycles);

	ADC_Cmd(ADC1, ENABLE);

}

